package com.company;

public class EvenNumbers_Without_If {

    public static void main(String[] args) {


        //print even numbers from 0 to 10 without using if condition

        System.out.println("Even Numbers are  ---> ");

        for (int i = 0; i <= 10; i += 2) {
            System.out.println(i + " ");
        }
    }
}
